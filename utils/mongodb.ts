import { Db, MongoClient, MongoClientOptions } from "mongodb";

const MONGODB_URI =
  "mongodb+srv://server:server@serverlessinstance0.egpu1.mongodb.net?retryWrites=true&w=majority";
const MONGODB_DB = "edneed";

let cachedClient: MongoClient | null = null;
let cachedDb: Db | null = null;

export async function connectToDatabase() {
  // check the cached.
  if (cachedClient && cachedDb) {
    // load from cache
    return {
      client: cachedClient,
      db: cachedDb,
    };
  }

  // Connect to cluster
  let client = new MongoClient(MONGODB_URI);
  await client.connect();
  let db = client.db(MONGODB_DB);

  // set cache
  cachedClient = client;
  cachedDb = db;

  return {
    client: cachedClient,
    db: cachedDb,
  };
}
